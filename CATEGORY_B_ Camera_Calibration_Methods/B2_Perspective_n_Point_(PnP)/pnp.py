def main():
    ap = argparse.ArgumentParser(
        description="Method B4: Perspective-n-Point (PnP)",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__)

    ap.add_argument("--demo",            action="store_true")
    ap.add_argument("--generate_marker", action="store_true")
    ap.add_argument("--checkerboard",    action="store_true")
    ap.add_argument("--aruco",           action="store_true")
    ap.add_argument("--manual",          action="store_true")
    ap.add_argument("--compare_solvers", action="store_true")

    ap.add_argument("--image",     default="images/method_b4_pnp/demo_scene.jpg")
    ap.add_argument("--calib",     default="calib_data.npz")
    ap.add_argument("--method",    default="epnp",
                    choices=["iterative","epnp","ippe"])
    ap.add_argument("--ransac",    action="store_true")

    ap.add_argument("--board_w",   type=int,   default=7)
    ap.add_argument("--board_h",   type=int,   default=5)
    ap.add_argument("--sq_cm",     type=float, default=2.5)
    ap.add_argument("--marker_cm", type=float, default=10.0)

    ap.add_argument("--world_pts", type=float, nargs="+",
                    help="3D world points: X1 Y1 Z1 X2 Y2 Z2 ...")
    ap.add_argument("--image_pts", type=float, nargs="+",
                    help="2D image points: u1 v1 u2 v2 ...")
    ap.add_argument("--object_pts", type=float, nargs="+", default=None,
                    help="Extra pixel points on the SAME plane to measure "
                         "(e.g. your object's endpoints): x1 y1 x2 y2 ...")
    ap.add_argument("--interactive_object", action="store_true",
                    help="Click object points on image instead of typing them")

    args = ap.parse_args()

    print("\n" + "═"*54)
    print("  METHOD B4 — Perspective-n-Point (PnP)")
    print("═"*54)

    # ── generate ArUco marker ─────────────────────────────────────
    if args.generate_marker:
        generate_aruco_marker()
        if not (args.demo or args.aruco or args.checkerboard or args.manual):
            return

    # ── demo mode ─────────────────────────────────────────────────
    if args.demo or not any([args.checkerboard, args.aruco, args.manual]):
        print("\n  Running demo with synthetic scene...")
        demo_path = "images/method_b4_pnp/demo_scene.jpg"
        img_demo, world_pts, img_pts, sq_cm = make_demo_scene(demo_path)

        K, D = load_or_default_calibration(
            args.calib, img_demo.shape)

        if args.compare_solvers:
            compare_all_solvers(img_demo, world_pts, img_pts, K, D)
        else:
            run_pnp(img_demo, world_pts, img_pts, K, D,
                    method=args.method,
                    use_ransac=args.ransac)
        print("\n  Demo complete. Check results/ folder.")
        return

    # ── load calibration ──────────────────────────────────────────
    img_tmp = cv2.imread(args.image)
    K, D = load_or_default_calibration(
        args.calib,
        img_tmp.shape if img_tmp is not None else None)

    # ── checkerboard mode ─────────────────────────────────────────
    if args.checkerboard:
        run_pnp_checkerboard(args.image, args.board_w, args.board_h,
                              args.sq_cm, K, D, args.method)

    # ── ArUco mode ────────────────────────────────────────────────
    elif args.aruco:
        obj_pixel_pts = None
        if args.object_pts:
            flat = args.object_pts
            obj_pixel_pts = [(flat[i], flat[i+1])
                              for i in range(0, len(flat)-1, 2)]
        run_pnp_aruco(args.image, args.marker_cm, K, D, args.method,
                      object_pixel_pts=obj_pixel_pts,
                      interactive_object=args.interactive_object)

    # ── manual correspondences ────────────────────────────────────
    elif args.manual:
        if not args.world_pts or not args.image_pts:
            print("  [!] Provide --world_pts and --image_pts")
            return

        w_flat = args.world_pts
        i_flat = args.image_pts

        world_pts = np.array(w_flat, dtype=np.float32).reshape(-1,3)
        img_pts   = np.array(i_flat, dtype=np.float32).reshape(-1,2)

        if len(world_pts) != len(img_pts):
            print(f"  [!] Points mismatch: "
                  f"{len(world_pts)} world vs {len(img_pts)} image")
            return

        img = cv2.imread(args.image)
        if img is None:
            img = np.ones((600,800,3),dtype=np.uint8)*200

        if args.compare_solvers:
            compare_all_solvers(img, world_pts, img_pts, K, D)
        else:
            rvec, tvec = run_pnp(img, world_pts, img_pts, K, D,
                    method=args.method, use_ransac=args.ransac)

            # ── measure additional object points on same plane ──────
            obj_pixel_pts = None
            if args.interactive_object:
                obj_pixel_pts = interactive_object_points(args.image)
            elif args.object_pts:
                flat = args.object_pts
                obj_pixel_pts = [(flat[i], flat[i+1])
                                  for i in range(0, len(flat)-1, 2)]

            if obj_pixel_pts and rvec is not None:
                measure_object_points(img, K, D, rvec, tvec, obj_pixel_pts)



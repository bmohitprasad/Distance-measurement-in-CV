# ── solver comparison ──────────────────────────────────────────────────────────

def compare_all_solvers(image, world_pts, img_pts, K, D):
    """Run all available solvers and compare their reprojection errors."""
    print("\n" + "═"*54)
    print("  B4 — Solver Comparison")
    print("═"*54)

    solvers = ["iterative", "epnp", "ippe"]
    results = {}

    for method in solvers:
        try:
            rvec, tvec, R, ok = solve_pnp(world_pts, img_pts, K, D, method)
            if ok:
                mean_err, max_err, _ = compute_reprojection_error(
                    world_pts, img_pts, rvec, tvec, K, D)
                dist = float(np.linalg.norm(tvec))
                results[method] = {
                    "mean_err": mean_err,
                    "max_err":  max_err,
                    "dist":     dist,
                    "rvec":     rvec,
                    "tvec":     tvec,
                }
                print(f"  {method.upper():<12} "
                      f"reproj={mean_err:.3f}px  "
                      f"dist={dist:.2f}cm")
            else:
                print(f"  {method.upper():<12} FAILED")
        except Exception as e:
            print(f"  {method.upper():<12} ERROR: {e}")

    # RANSAC
    try:
        rvec_r, tvec_r, R_r, ok_r, _ = solve_pnp_ransac(world_pts, img_pts, K, D)
        if ok_r:
            me, mx, _ = compute_reprojection_error(
                world_pts, img_pts, rvec_r, tvec_r, K, D)
            dist_r = float(np.linalg.norm(tvec_r))
            results["ransac"] = {"mean_err":me,"max_err":mx,"dist":dist_r}
            print(f"  {'RANSAC':<12} reproj={me:.3f}px  dist={dist_r:.2f}cm")
    except Exception as e:
        print(f"  {'RANSAC':<12} ERROR: {e}")

    # Build comparison visualisation
    if results:
        vis_panels = []
        for method, res in results.items():
            if "rvec" in res:
                panel = image.copy()
                _, _, proj = compute_reprojection_error(
                    world_pts, img_pts,
                    res["rvec"], res["tvec"], K, D)
                for pt in proj:
                    cv2.circle(panel,tuple(pt.astype(int)),5,(0,80,220),-1)
                for pt in img_pts.reshape(-1,2):
                    cv2.circle(panel,tuple(pt.astype(int)),7,(0,200,80),2)
                draw_axes(panel,K,D,res["rvec"],res["tvec"],4.0)
                put_text(panel,
                         f"{method.upper()} | err={res['mean_err']:.2f}px",
                         (8,22),(255,220,80),0.5)
                vis_panels.append(panel)

        if len(vis_panels) >= 2:
            h,w = vis_panels[0].shape[:2]
            rows = []
            for i in range(0,len(vis_panels),2):
                pair = vis_panels[i:i+2]
                if len(pair)==2:
                    rows.append(np.hstack(pair))
                else:
                    blank = np.zeros_like(pair[0])
                    rows.append(np.hstack([pair[0],blank]))
            grid = np.vstack(rows)
            save_result(grid,"solver_comparison")

    print("═"*54)
    return results

# ── checkerboard-based PnP ────────────────────────────────────────────────────

def run_pnp_checkerboard(image_path, board_w, board_h, sq_cm, K, D,
                          method="epnp"):
    """Detect checkerboard corners and solve PnP."""
    img  = cv2.imread(image_path)
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    board_size = (board_w, board_h)

    # Build 3D world points
    objp = np.zeros((board_w*board_h,3), np.float32)
    objp[:,:2] = np.mgrid[0:board_w,0:board_h].T.reshape(-1,2)
    objp *= sq_cm

    criteria = (cv2.TERM_CRITERIA_EPS+cv2.TERM_CRITERIA_MAX_ITER,30,0.001)
    ret, corners = cv2.findChessboardCorners(gray, board_size, None)

    if not ret:
        print("  [!] Checkerboard not detected. Check image quality.")
        return None, None

    corners = cv2.cornerSubPix(gray,corners,(11,11),(-1,-1),criteria)

    print(f"  Checkerboard detected: {board_w}×{board_h} = "
          f"{board_w*board_h} corners")

    return run_pnp(img, objp, corners.reshape(-1,2), K, D,
                   method=method, label="Checkerboard PnP")

# ── ArUco-based PnP ──────────────────────────────────────────────────────────

def run_pnp_aruco(image_path, marker_cm, K, D, method="ippe",
                   object_pixel_pts=None, interactive_object=False):
    """Detect ArUco marker and solve PnP using its 4 corners.
    If object_pixel_pts given (or interactive_object=True), also
    measures those points on the same plane as the marker.
    """
    try:
        aruco_dict   = cv2.aruco.getPredefinedDictionary(cv2.aruco.DICT_4X4_50)
        aruco_params = cv2.aruco.DetectorParameters()
        detector     = cv2.aruco.ArucoDetector(aruco_dict, aruco_params)
    except AttributeError:
        print("  [!] ArUco requires opencv-contrib-python")
        print("      pip install opencv-contrib-python")
        return None, None

    img  = cv2.imread(image_path)
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

    corners_all, ids, _ = detector.detectMarkers(gray)

    if ids is None or len(ids) == 0:
        print("  [!] No ArUco markers detected.")
        return None, None

    print(f"  ArUco markers detected: {len(ids)} (IDs: {ids.flatten()})")

    # Use first detected marker
    corners_2d = corners_all[0].reshape(-1,2)   # 4 x 2
    # OpenCV ArUco corner order is: top-left, top-right,
    # bottom-right, bottom-left (clockwise, as seen in image).
    # World points must follow the SAME order for correct pose.
    s = marker_cm
    world_pts = np.float32([
        [0, 0, 0],   # top-left
        [s, 0, 0],   # top-right
        [s, s, 0],   # bottom-right
        [0, s, 0],   # bottom-left
    ])

    cv2.aruco.drawDetectedMarkers(img, corners_all, ids)
    rvec, tvec = run_pnp(img, world_pts, corners_2d, K, D,
                   method=method, label="ArUco PnP")

    # ── measure additional object points using solved pose ─────
    if rvec is not None:
        obj_pts = object_pixel_pts
        if interactive_object:
            obj_pts = interactive_object_points(image_path)
        if obj_pts:
            measure_object_points(img, K, D, rvec, tvec, obj_pts)

    return rvec, tvec


"""
METHOD B4: Perspective-n-Point (PnP)
=======================================
WHAT IT DOES:
    Given n 3D world points and their corresponding 2D image projections,
    PnP recovers the camera POSE (rotation R + translation t) relative to
    the object. Once pose is known:
        - Distance from camera to object = ||t|| (magnitude of translation)
        - Object dimensions from 3D point differences
        - Any real-world measurement directly from 3D coordinates

MATHEMATICAL FOUNDATION:
    The pinhole projection equation:
        s × [u,v,1]ᵀ = K × [R|t] × [X,Y,Z,1]ᵀ

    Where:
        K       = camera intrinsic matrix (3×3)
        R       = rotation matrix (3×3) — how object is oriented
        t       = translation vector (3×1) — where object is in space
        [X,Y,Z] = 3D world point (known)
        [u,v]   = 2D image point (detected)
        s       = scale factor (depth)

    PnP solves for [R|t] given K, world points, and image points.

SOLVING METHODS (all implemented here):
    SOLVEPNP_ITERATIVE  — Levenberg-Marquardt, most accurate, needs good init
    SOLVEPNP_EPNP       — Efficient PnP, fast, works with n>=4 points
    SOLVEPNP_P3P        — Minimal solver, exactly 3 points, multiple solutions
    SOLVEPNP_IPPE       — Planar objects only, very fast
    SOLVEPNP_IPPE_SQUARE— Square planar targets (ArUco markers)
    RANSAC              — Robust to outlier point correspondences

LOSS FUNCTION:
    Reprojection error (same as calibration):
        L = Σ ||p_image - π(K, R, t, P_world)||²

    Where π() is the projection function.
    After solving, reprojects world points back and measures pixel error.
    Good solution: error < 2.0 pixels for measurement use.

WHAT IMAGE TO USE:

    OPTION A — Checkerboard / known pattern (most accurate):
        Same checkerboard used in B1/B2 calibration.
        Photograph from any angle.
        Known 3D corner positions → detected 2D corners → solve PnP.

    OPTION B — ArUco marker (easiest setup):
        Print an ArUco marker (generated by --generate_marker).
        Place it on or beside your object.
        Code detects corners automatically — no manual clicking.

    OPTION C — Manual 3D-2D correspondences:
        Any image where you know at least 4 points' real 3D positions.
        Example: corners of a known-size box, corners of an A4 sheet,
                 four sticker markers at measured positions.

    OPTION D — Demo mode:
        Run --demo for fully synthetic test, no images needed.

WHAT YOU NEED BEFORE RUNNING:
    Camera calibration data (calib_data.npz from B1).
    If you don't have it: run --demo (uses synthetic K) or
    run B1 first: python method_b1_pinhole.py --calibrate

FOLDER STRUCTURE:
    images/
      method_b4_pnp/
        object.jpg    ← image of object with known reference points

HOW TO RUN:

    # Full demo (no images, no calibration file needed):
    python method_b4_pnp.py --demo

    # Generate a printable ArUco marker:
    python method_b4_pnp.py --generate_marker

    # Run with ArUco marker in image (auto-detects corners):
    python method_b4_pnp.py --aruco \
        --image images/method_b4_pnp/aruco_scene.jpg \
        --marker_cm 10.0 \
        --calib calib_data.npz

    # Run with checkerboard image:
    python method_b4_pnp.py --checkerboard \
        --image images/method_b4_pnp/checker.jpg \
        --board_w 9 --board_h 6 --sq_cm 2.5 \
        --calib calib_data.npz

    # Run with manually specified point correspondences:
    python method_b4_pnp.py --manual \
        --world_pts  0 0 0   10 0 0   10 8 0   0 8 0 \
        --image_pts  120 340  450 335  448 210  122 215 \
        --calib calib_data.npz \
        --image images/method_b4_pnp/object.jpg

    # Compare all PnP solvers on same data:
    python method_b4_pnp.py --demo --compare_solvers

PARAMETERS:
    --calib           : path to calibration .npz from B1 (default: calib_data.npz)
    --image           : input image path
    --mode            : iterative | epnp | p3p | ippe (default: epnp)
    --compare_solvers : run all solver methods and compare results
    --marker_cm       : ArUco marker real size in cm
    --board_w/h       : checkerboard inner corners
    --sq_cm           : checkerboard square size in cm
    --world_pts       : manual 3D points as flat list: X1 Y1 Z1 X2 Y2 Z2 ...
    --image_pts       : corresponding 2D points: u1 v1 u2 v2 ...
    --measure_pts     : two 3D world points to measure distance between
    --demo            : synthetic test, no files needed
    --generate_marker : save a printable ArUco marker image
"""


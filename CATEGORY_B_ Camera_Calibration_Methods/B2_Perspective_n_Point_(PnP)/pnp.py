SOLVER_MAP = {
    "iterative": cv2.SOLVEPNP_ITERATIVE,
    "epnp":      cv2.SOLVEPNP_EPNP,
    "ippe":      cv2.SOLVEPNP_IPPE,
}

def solve_pnp(world_pts, img_pts, K, D, method="epnp"):
    """
    Solve PnP — find camera pose [R|t] given 3D-2D correspondences.

    Args:
        world_pts : (N,3) float32 — 3D points in object/world coordinates (cm)
        img_pts   : (N,2) float32 — corresponding 2D image pixel coordinates
        K         : (3,3) camera matrix
        D         : distortion coefficients
        method    : solver name string

    Returns:
        rvec      : rotation vector (3,1) — Rodrigues form
        tvec      : translation vector (3,1) — camera position in cm
        R         : rotation matrix (3,3)
        success   : bool
    """
    solver = SOLVER_MAP.get(method, cv2.SOLVEPNP_EPNP)

    n = len(world_pts)
    if method == "p3p" and n != 4:
        print(f"  [!] P3P requires exactly 4 points, got {n}. Switching to EPNP.")
        solver = cv2.SOLVEPNP_EPNP

    success, rvec, tvec = cv2.solvePnP(
        world_pts.astype(np.float32),
        img_pts.astype(np.float32),
        K, D,
        flags=solver)

    if not success:
        return None, None, None, False

    # Convert Rodrigues rotation vector → rotation matrix
    R, _ = cv2.Rodrigues(rvec)
    return rvec, tvec, R, True

def solve_pnp_ransac(world_pts, img_pts, K, D,
                      reprojection_thresh=8.0, iterations=200):
    """
    RANSAC PnP — robust to outlier correspondences.
    Returns inlier mask in addition to pose.
    """
    success, rvec, tvec, inliers = cv2.solvePnPRansac(
        world_pts.astype(np.float32),
        img_pts.astype(np.float32),
        K, D,
        iterationsCount=iterations,
        reprojectionError=reprojection_thresh,
        confidence=0.99)

    if not success or inliers is None:
        return None, None, None, False, np.array([])

    R, _ = cv2.Rodrigues(rvec)
    inlier_mask = inliers.flatten()
    n_inliers = len(inlier_mask)
    n_total   = len(world_pts)
    print(f"  RANSAC: {n_inliers}/{n_total} inliers "
          f"({100*n_inliers/n_total:.1f}%)")
    return rvec, tvec, R, True, inlier_mask

# ── measurement from pose ──────────────────────────────────────────────────────

def measure_from_pose(rvec, tvec, R, world_pts):
    """
    Extract all useful measurements from solved pose.

    Camera centre in world coords = -R^T × t
    Distance to origin = ||t||
    """
    # Camera position in world frame
    cam_pos_world = -R.T @ tvec.flatten()

    # Distance from camera to world origin
    dist_to_origin = np.linalg.norm(tvec)

    # Distances between world points (ground truth 3D distances)
    pt_distances = {}
    n = len(world_pts)
    for i in range(n):
        for j in range(i+1, n):
            d = np.linalg.norm(world_pts[i] - world_pts[j])
            pt_distances[f"P{i+1}→P{j+1}"] = d

    # Euler angles from rotation matrix
    euler = rotation_matrix_to_euler(R)

    # Object-to-camera vector direction
    z_axis_world = R.T @ np.array([0,0,1])

    return {
        "dist_to_origin_cm":  dist_to_origin,
        "cam_pos_world":      cam_pos_world,
        "euler_deg":          euler,
        "pt_distances_cm":    pt_distances,
        "z_axis_world":       z_axis_world,
    }

# ── full pipeline ──────────────────────────────────────────────────────────────

def run_pnp(image, world_pts, img_pts, K, D,
            method="epnp", use_ransac=False,
            draw_axis=True, draw_box=True,
            axis_len_cm=5.0, label="B4 PnP"):
    """
    Full PnP pipeline: solve → measure → visualise → report.
    """
    vis = image.copy()
    h, w = vis.shape[:2]

    print(f"\n  Solver: {method.upper()}"
          f"{'  + RANSAC' if use_ransac else ''}")
    print(f"  Points: {len(world_pts)} correspondences")

    # ── solve ──────────────────────────────────────────────────────
    inlier_mask = None
    if use_ransac:
        rvec, tvec, R, ok, inlier_mask = solve_pnp_ransac(
            world_pts, img_pts, K, D)
    else:
        rvec, tvec, R, ok = solve_pnp(world_pts, img_pts, K, D, method)

    if not ok:
        print("  [!] PnP failed. Check point correspondences.")
        return None, None

    # ── reprojection error ─────────────────────────────────────────
    mean_err, max_err, proj_pts = compute_reprojection_error(
        world_pts, img_pts, rvec, tvec, K, D)

    quality = ("EXCELLENT" if mean_err < 1.0 else
               "GOOD"      if mean_err < 3.0 else
               "ACCEPTABLE" if mean_err < 8.0 else
               "POOR")

    # ── measurements ───────────────────────────────────────────────
    meas = measure_from_pose(rvec, tvec, R, world_pts)

    # ── visualise detected vs projected points ─────────────────────
    for i, (pt_det, pt_proj) in enumerate(zip(
            img_pts.reshape(-1,2), proj_pts)):

        # Detected point (filled circle)
        is_inlier = (inlier_mask is None or i in inlier_mask)

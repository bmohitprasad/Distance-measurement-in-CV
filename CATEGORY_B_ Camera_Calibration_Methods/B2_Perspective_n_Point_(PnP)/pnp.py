def rotation_matrix_to_euler(R):
    """Convert 3×3 rotation matrix to Euler angles (degrees)."""
    sy = math.sqrt(R[0,0]**2 + R[1,0]**2)
    singular = sy < 1e-6
    if not singular:
        x = math.atan2( R[2,1], R[2,2])
        y = math.atan2(-R[2,0], sy)
        z = math.atan2( R[1,0], R[0,0])
    else:
        x = math.atan2(-R[1,2], R[1,1])
        y = math.atan2(-R[2,0], sy)
        z = 0
    return np.degrees([x, y, z])

def compute_reprojection_error(world_pts, img_pts, rvec, tvec, K, D):
    """Compute mean reprojection error in pixels."""
    projected, _ = cv2.projectPoints(
        world_pts.astype(np.float32),
        rvec, tvec, K, D)
    projected = projected.reshape(-1, 2)
    img_pts_   = img_pts.reshape(-1, 2)
    errors = np.linalg.norm(projected - img_pts_, axis=1)
    return float(np.mean(errors)), float(np.max(errors)), projected

def draw_axes(img, K, D, rvec, tvec, length_cm=5.0):
    """
    Draw 3D coordinate axes on image at object origin.
    Red=X, Green=Y, Blue=Z
    """
    axis_pts = np.float32([
        [0,       0,       0      ],   # origin
        [length_cm, 0,     0      ],   # X
        [0,       length_cm, 0    ],   # Y
        [0,       0,       -length_cm] # Z (into scene)
    ])
    img_pts, _ = cv2.projectPoints(axis_pts, rvec, tvec, K, D)
    img_pts = img_pts.astype(int).reshape(-1, 2)

    origin = tuple(img_pts[0])
    cv2.arrowedLine(img, origin, tuple(img_pts[1]), (0,0,220),   2, tipLength=0.2)
    cv2.arrowedLine(img, origin, tuple(img_pts[2]), (0,200,0),   2, tipLength=0.2)
    cv2.arrowedLine(img, origin, tuple(img_pts[3]), (220,80,0),  2, tipLength=0.2)
    put_text(img, "X", (img_pts[1][0]+5, img_pts[1][1]), (0,0,220),   0.45)
    put_text(img, "Y", (img_pts[2][0]+5, img_pts[2][1]), (0,200,0),   0.45)
    put_text(img, "Z", (img_pts[3][0]+5, img_pts[3][1]), (220,80,0),  0.45)
    return img

def draw_cube(img, K, D, rvec, tvec, size_cm=5.0):
    """Draw a 3D cube overlay at the object origin."""
    s = size_cm
    cube_pts = np.float32([
        [0,0,0],[s,0,0],[s,s,0],[0,s,0],   # bottom face
        [0,0,-s],[s,0,-s],[s,s,-s],[0,s,-s] # top face
    ])
    img_pts, _ = cv2.projectPoints(cube_pts, rvec, tvec, K, D)
    img_pts = img_pts.astype(int).reshape(-1, 2)

    # Bottom face edges
    for i,j in [(0,1),(1,2),(2,3),(3,0)]:
        cv2.line(img, tuple(img_pts[i]), tuple(img_pts[j]), (0,200,80), 1)
    # Top face edges
    for i,j in [(4,5),(5,6),(6,7),(7,4)]:
        cv2.line(img, tuple(img_pts[i]), tuple(img_pts[j]), (0,140,220), 1)
    # Vertical edges
    for i in range(4):
        cv2.line(img, tuple(img_pts[i]), tuple(img_pts[i+4]), (200,200,0), 1)
    return img

# ── calibration loader ─────────────────────────────────────────────────────────

def load_or_default_calibration(calib_path, img_shape=None):
    """Load calibration from file, or build a reasonable default."""
    if calib_path and os.path.exists(calib_path):
        data = np.load(calib_path)
        K = data["camera_matrix"]
        D = data["dist_coeffs"]
        print(f"  Calibration loaded: {calib_path}")
        return K, D
    else:
        if calib_path:
            print(f"  [!] Calibration not found: {calib_path}")
        print("  Using approximate default camera matrix.")
        h = img_shape[0] if img_shape else 600
        w = img_shape[1] if img_shape else 800
        f  = max(w, h) * 1.2
        K  = np.array([[f,0,w/2],[0,f,h/2],[0,0,1]], dtype=np.float64)
        D  = np.zeros((1,5), dtype=np.float64)
        return K, D

# ── ArUco marker generation ────────────────────────────────────────────────────

def generate_aruco_marker(marker_id=0, size_px=400,
                           out_path="print_this_aruco.png"):
    """Generate a printable ArUco marker."""
    try:
        aruco_dict = cv2.aruco.getPredefinedDictionary(cv2.aruco.DICT_4X4_50)
        marker_img = np.zeros((size_px, size_px), dtype=np.uint8)
        cv2.aruco.generateImageMarker(aruco_dict, marker_id, size_px, marker_img)
        # Add white border
        canvas = np.ones((size_px+80, size_px+80), dtype=np.uint8)*255
        canvas[40:40+size_px, 40:40+size_px] = marker_img
        cv2.imwrite(out_path, canvas)
        print(f"\n  ArUco marker saved → {out_path}")
        print(f"  Marker ID: {marker_id} | Dictionary: DICT_4X4_50")
        print(f"  Print at known size. Measure printed marker width → use as --marker_cm")
        return canvas
    except AttributeError:
        print("  [!] ArUco not available in this OpenCV build.")
        print("      Install: pip install opencv-contrib-python")
        return None

# ── synthetic demo ─────────────────────────────────────────────────────────────

def make_demo_scene(out_path="images/method_b4_pnp/demo_scene.jpg",
                    board_w=7, board_h=5, sq_px=60):
    """
    Create synthetic scene: checkerboard at a known pose.
    Returns image + ground-truth 3D/2D correspondences.
    """
    os.makedirs(os.path.dirname(out_path), exist_ok=True)

    IMG_W, IMG_H = 800, 600
    canvas = np.ones((IMG_H, IMG_W, 3), dtype=np.uint8) * 200

    # Background gradient
    for y in range(IMG_H):
        v = int(190 + 25*(y/IMG_H))
        canvas[y,:] = (v, v+5, v+10)

    # Draw checkerboard at slight perspective
    cols, rows = board_w+1, board_h+1
    margin_x, margin_y = 120, 100
    sq = sq_px

    # Apply slight perspective skew
    for r in range(rows):
        for c in range(cols):
            if (r+c) % 2 == 0:
                # Trapezoid perspective: compress top
                persp = 0.85 + 0.15*(r/rows)
                x1 = int(margin_x + c*sq*persp + r*3)
                y1 = int(margin_y + r*sq*0.88)
                x2 = int(x1 + sq*persp)
                y2 = int(y1 + sq*0.88)
                cv2.rectangle(canvas, (x1,y1),(x2,y2),(30,30,30),-1)

    # Draw a target object (rice grain shape)
    cv2.ellipse(canvas,(580,420),(80,30),10,0,360,(120,160,80),-1)
    cv2.ellipse(canvas,(580,420),(80,30),10,0,360,(60,100,40),2)
    put_text(canvas,"Target object",(510,395),(60,100,40),0.4)

    # Add subtle noise
    noise = np.random.randint(-6,6,canvas.shape,dtype=np.int16)
    canvas = np.clip(canvas.astype(np.int16)+noise,0,255).astype(np.uint8)

    cv2.imwrite(out_path, canvas)

    # Build synthetic 3D-2D correspondences for 4 corners of the board
    # Ground truth object corners in world coords (cm)
    sq_cm = 2.5
    world_pts = np.float32([
        [0,                   0,                   0],
        [board_w * sq_cm,     0,                   0],
        [board_w * sq_cm,     board_h * sq_cm,     0],
        [0,                   board_h * sq_cm,     0],
        [board_w/2 * sq_cm,   board_h/2 * sq_cm,   0],  # centre
    ])

    # Corresponding approximate image pixels (from our drawn board)
    img_pts = np.float32([
        [120,  100],
        [120 + (board_w)*sq_px*0.85+board_h*3,   100],
        [120 + board_w*sq_px*1.0 +board_h*3,     100+board_h*sq_px*0.88],
        [120,                                      100+board_h*sq_px*0.88],
        [120 + board_w*sq_px*0.5,                 100+board_h*sq_px*0.44],
    ])

    return canvas, world_pts, img_pts, sq_cm

# ── core PnP solver ────────────────────────────────────────────────────────────

def pixel_to_world_on_plane(pixel_pt, K, D, rvec, tvec):
    """
    Given a pixel point known to lie on the Z=0 world plane
    (same plane as your reference points), back-project it to
    find its real-world (X, Y, 0) coordinate.

    This lets you measure ANY point in the image once pose is
    solved from 4+ known reference points on that plane.
    """
    R, _ = cv2.Rodrigues(rvec)

    # Undistort the pixel point first
    pt = np.array([[pixel_pt]], dtype=np.float32)
    undist_pt = cv2.undistortPoints(pt, K, D, P=K)
    u, v = undist_pt[0,0]

    # Ray direction in camera coords
    K_inv = np.linalg.inv(K)
    ray_cam = K_inv @ np.array([u, v, 1.0])

    # Rotate ray into world coords
    ray_world = R.T @ ray_cam
    cam_pos_world = -R.T @ tvec.flatten()

    # Intersect with Z=0 plane:
    # cam_pos_world[2] + s * ray_world[2] = 0
    if abs(ray_world[2]) < 1e-8:
        return None
    s = -cam_pos_world[2] / ray_world[2]
    world_pt = cam_pos_world + s * ray_world
    return world_pt  # [X, Y, 0] in cm

def measure_object_points(image, K, D, rvec, tvec, pixel_points, labels=None):
    """
    Measure real-world distances between arbitrary clicked pixel
    points, using the pose already solved from reference points.

    Args:
        pixel_points : list of (x,y) pixel coords on the same
                       plane as your reference points (e.g. A4 sheet)
        labels        : optional names for each point
    """
    if labels is None:
        labels = [f"O{i+1}" for i in range(len(pixel_points))]

    world_coords = []
    for pt in pixel_points:
        w = pixel_to_world_on_plane(pt, K, D, rvec, tvec)
        world_coords.append(w)

    vis = image.copy()
    metrics = {"─ Object Points (world coords) ─": ""}

    for i, (px, w) in enumerate(zip(pixel_points, world_coords)):
        cv2.circle(vis, tuple(map(int,px)), 7, (0,140,255), 2)
        put_text(vis, labels[i], (int(px[0])+9, int(px[1])-9),
                 (0,140,255), 0.45)
        if w is not None:
            metrics[f"  {labels[i]} pixel"] = f"{px}"
            metrics[f"  {labels[i]} world (X,Y,cm)"] = f"({w[0]:.3f}, {w[1]:.3f})"

    metrics["─ Object Distances ─"] = ""
    n = len(pixel_points)
    for i in range(n):
        for j in range(i+1, n):
            if world_coords[i] is not None and world_coords[j] is not None:
                d = np.linalg.norm(world_coords[i] - world_coords[j])
                cv2.line(vis, tuple(map(int,pixel_points[i])),
                         tuple(map(int,pixel_points[j])), (0,220,120), 2)
                mx = int((pixel_points[i][0]+pixel_points[j][0])/2)
                my = int((pixel_points[i][1]+pixel_points[j][1])/2) - 10
                put_text(vis, f"{d:.3f}cm", (mx,my), (0,220,120), 0.5)
                metrics[f"  {labels[i]}→{labels[j]}"] = f"► {d:.4f} cm"

    save_result(vis, "object_measurement")
    show_metrics("B4 — Object Measurement (via solved pose)", metrics)
    return world_coords, metrics

